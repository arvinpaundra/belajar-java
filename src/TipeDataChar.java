public class TipeDataChar {
  public static void main(String[] args) {
    // a character is using single-quote ('')
    char a = 'A';
    char r = 'R';
    char v = 'V';
    char i = 'I';
    char n = 'N';

    System.out.print(a);
    System.out.print(r);
    System.out.print(v);
    System.out.print(i);
    System.out.print(n);
  }
}
